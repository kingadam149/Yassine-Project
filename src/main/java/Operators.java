public class Operators {

    public static void main(String[] args) {
        int a, b = 10, c = 5;
        a = b + c;
        System.out.println(a);
        a = b - c;
        System.out.println(a);
        a = b * c;
        System.out.println(a);
        a = b / c;
        System.out.println(a);
        a = b % c;
        System.out.println(a);
        b++;
        System.out.println(b);
        c--;
        System.out.println(c);


    }
}
