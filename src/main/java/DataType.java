public class DataType {

    public static void main(String[] args){
        int a = 70;
        int b = 15;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        a++;
        b--;
        System.out.println(a);
        System.out.println(b);


        int g = 25;
        int h = 2;
        System.out.println(g/h);
        System.out.println(g%h);
    }
}


